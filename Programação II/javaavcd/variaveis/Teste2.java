package variaveis;

public class Teste2 {
	public static void main(String[] args) {
		int i = 5;
		System.out.println(i);
		
		int j = i++; //pós incremento
		System.out.println(i);
		System.out.println(j);
		
		
		int m = 10;
		System.out.println(m);
		
		int n = ++m; //pré incremento
		System.out.println(m);
		System.out.println(n);
				
	}

}
